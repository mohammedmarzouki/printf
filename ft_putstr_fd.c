#include "libftprintf.h"

void	ft_putstr_fd(char *s, int fd,int n)
{
	int i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0' && n)
		{
			ft_putchar_fd(s[i], fd);
			i++;
			n--;
		}
	}
}
